# Erstellen Sie ein Programm, das die Zahlen von 1 bis 15 ausgibt. Verwenden Sie dabei
# eine kopfgesteuerte Schleife

i = 0 # Initialisiert die Zählvariable i mit 0

while i < 15: # Kopfgesteuerte Schleife: Läuft, solange i kleiner als 15 ist
    i += 1 # Erhöht i um 1 (i wird zu 1, 2, ..., 15)
    print(i) # Gibt den aktuellen Wert von i aus
